Function Enter-Site {

<#

.SYNOPSIS

Performs a login to a given SCCM Site Server.


.DESCRIPTION

Performs a login to a SCCM Site Server and switches to the required PSDrive. The user initiating the module must have the required permissions on the SCCM Site Server.

For more information about Cmdlets see 'about_Functions_CmdletBindingAttribute'.

Note: ConfigurationManager by default is not in the $ENV:PSModulePath, 
so you have to either add it to that variable or create a junction like this:
PS > junction.exe ConfigurationManager "C:\Program Files (x86)\Microsoft Configuration Manager\AdminConsole\bin"


.OUTPUTS

This Cmdlet returns the name of the SCCM Server. On failure the string contains $null.


.PARAMETER SCCMSite

The Sitecode of the SCCM Site to connect to.


.PARAMETER SCCMModulePath

The Path to the SCCMModulePath.


.EXAMPLE

Perform a login to a SCCM Site.

Connect-SCCMSite -SiteCode SITE1


.LINK

Online Version: http://dfch.biz/biz/dfch/PS/Sccm/Utilities/Enter-Site/


.NOTES

See module manifest for dependencies and further requirements.


#>

Param
(

	[Parameter(Mandatory = $true, Position = 0)]
	[ValidateNotNullOrEmpty()]
	[Alias("SiteCode")]
	[string] $SCCMSiteCode
)


try
{
	
	$datBegin = [datetime]::Now;
	[string] $fn = $MyInvocation.MyCommand.Name;
	Log-Debug -fn $fn -msg ("CALL")
	
	$PSD = Get-PSdrive -PSProvider CMSite -name $SCCMSiteCode -ea SilentlyContinue
	if ( $PSD )
	{
		Log-Debug $fn ('PSProvider for CMSite found: {0}' -f $SCCMSiteCode )
		Log-Debug $fn ('Change Working directory: {0}' -f $PSD.Name )
		cd ("{0}:" -f ($PSD.Name) )
		
		
	} else {
		
		#Check if CMSite Provider is not loaded
		if( !( Get-PSdrive -PSProvider CMSite -ea SilentlyContinue) )
		{
			throw "No CMSite PSProvider available - The current user might not have the required SCCM permissions" 
		}
		Log-Debug $fn ('PSProvider not found: {0}' -f $SCCMSiteCode )
		
		#Check if the required ps drive is available now
		$PSD = Get-PSdrive -PSProvider CMSite -name $SCCMSiteCode -ea SilentlyContinue
		if( $null -eq $PSD )
		{
			#Check if any drive is available
			$PSD = Get-PSdrive -PSProvider CMSite -ea SilentlyContinue | Select -first 1
			$Site = get-CMSite -SiteCode $SCCMSiteCode
			
			if ( $Site )
			{
				Log-Debug $fn ( 'Site found: {0}, ServerName {1}' -f $SCCMSiteCode , $Site.ServerName)
				$PSD = New-PSDrive -Name $SCCMSiteCode -PSProvider CMSite -Root $Site.ServerName
				Log-Debug $fn ('Changing Working directory: {0}' -f $PSD.Name )
				cd ("{0}:" -f ($PSD.Name) )
			
			} else {
				Log-Error $fn ('Unable to connect to SCCM Site: {0}' -f $SCCMSiteCode )
				throw ( 'Unable to find SCCM Site with provided SiteCode: {0}' -f $SCCMSiteCode )
			}
		}
		
		Log-Debug $fn ('Changing Working directory: {0}' -f $PSD.Name )
		cd ("{0}:" -f ($PSD.Name) )
		
		
		
	}
} 
catch 
{
	Log-Error $fn ('Error occured: {0}' -f $_.Exception.Message )
	throw $_.Exception
}

} # function

if($MyInvocation.ScriptName) { Export-ModuleMember -Function Enter-Site; } 


